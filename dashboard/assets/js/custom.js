// CHARACTER COUNTER
const charInputs = document.querySelectorAll(`[data-input-type="textarea-counter"]`);
charInputs.forEach((input) => {
  let maxLenght, value, parent, counter, counterText;
  maxLenght = input.maxLength;
  parent = input.closest("div");
  counter = parent.querySelector("[data-counter-value]");
  if (typeof maxLenght === undefined || maxLenght === -1) {
    counter.innerHTML = "";
  } else {
    counter.innerHTML = `0/${maxLenght} karakter`;
  }

  input.addEventListener("input", (e) => {
    if (!maxLenght || maxLenght === -1) {
      counter.innerHTML = "";
    } else {
      value = e.target.value.length;
      counterText = `${value}/${maxLenght} karakter`;
      counter.innerHTML = counterText;
    }
  });
});
// CHARACTER COUNTER END

